public class Datatypes
{
    public static void main(String args[])
    {
        boolean b = true;
        char c = 'a';
        int i = 10;
        float f = 3.1416f;
        double d = 2.54123;

        System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
    }
}